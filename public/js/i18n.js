  // 获取翻译文本
  t(key, params = {}) {
    const lang = this.getCurrentLanguage();
    const text = this.translations[lang]?.[key] || this.translations[this.defaultLanguage]?.[key] || key;

    // 替换参数 {name} -> value
    if (Object.keys(params).length > 0) {
      let result = text;
      for (const [key, value] of Object.entries(params)) {
        result = result.replace(new RegExp(`{${key}}`, 'g'), value);
      }
      return result;
    }

    return text;
  }
